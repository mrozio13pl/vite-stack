#!/usr/bin/env node
import { globSync, mkdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import * as fontkit from 'fontkit';
import { parse as parseFrontmatter } from 'ultramatter';
import Slugger from 'github-slugger';

const readmePath = 'MAIN_README.md';

const root = new URL('..', import.meta.url).pathname;
const recipeDir = join(root, 'recipes');
const headingDir = join(root, 'recipe-headings');
const headingUrl = 'https://raw.githubusercontent.com/mrozio13pl/vite-stack/recipes/recipe-headings';
const headingFont = join(
    root,
    'node_modules/@fontsource/dela-gothic-one/files/dela-gothic-one-latin-400-normal.woff2',
);
const files = globSync('*.md', {
    cwd: recipeDir,
    exclude: ['creating-recipes.md']
}).sort();

const parse = (file) => {
    const { content, frontmatter = {} } = parseFrontmatter(
        readFileSync(join(recipeDir, file), 'utf8'),
    );
    const title = content.match(/^#\s+(.+)$/m)?.[1] ?? frontmatter.feature ?? file.replace(/\.md$/, '');
    const description = content
        .replace(/^#\s+.+$/m, '')
        .split(/^##\s+/m)[0]
        .trim();
    const prompt = content.match(/## Agent prompt\n\n```text\n([\s\S]*?)\n```/)?.[1]?.trim();

    return { file, title, description, prompt, ...frontmatter };
};

const escapeXml = (text) =>
    String(text)
        .replaceAll('&', '&amp;')
        .replaceAll('<', '&lt;')
        .replaceAll('>', '&gt;')
        .replaceAll('"', '&quot;');

const renderHeading = (text, file, fill) => {
    const font = fontkit.openSync(headingFont);
    const fontSize = 48;
    const padding = 4;
    const scale = fontSize / font.unitsPerEm;
    const run = font.layout(text.toUpperCase());
    const width = Math.ceil(run.positions.reduce((sum, pos) => sum + pos.xAdvance, 0) * scale + padding * 2);
    const height = Math.ceil((font.ascent - font.descent) * scale + padding * 2);
    const baseline = padding + font.ascent * scale;
    let x = padding;
    const paths = run.glyphs
        .map((glyph, index) => {
            const pos = run.positions[index];
            const path = `<path d="${glyph.path.toSVG()}" transform="translate(${x + pos.xOffset * scale} ${baseline - pos.yOffset * scale}) scale(${scale} -${scale})"/>`;
            x += pos.xAdvance * scale;
            return path;
        })
        .join('\n    ');

    writeFileSync(
        join(headingDir, file),
        `<svg xmlns="http://www.w3.org/2000/svg" width="${width}" height="${height}" viewBox="0 0 ${width} ${height}" role="img" aria-label="${escapeXml(text)}">
  <!-- This file is auto-generated by scripts/update-main-readme.mjs. -->
  <g fill="${fill}">
    ${paths}
  </g>
</svg>
`,
    );
};

rmSync(headingDir, { recursive: true, force: true });
mkdirSync(headingDir, { recursive: true });
renderHeading('VITE-STACK', 'vite-stack-dark.svg', '#111827');
renderHeading('VITE-STACK', 'vite-stack-light.svg', '#f9fafb');
const headingSlugger = new Slugger();
const recipes = files
    .map(parse)
    .filter((recipe) => recipe.status !== 'draft')
    .map((recipe) => {
        const headingId = headingSlugger.slug(recipe.title);
        const base = recipe.file.replace(/\.md$/, '');
        const headingDark = `${base}-dark.svg`;
        const headingLight = `${base}-light.svg`;
        renderHeading(recipe.title, headingDark, '#111827');
        renderHeading(recipe.title, headingLight, '#f9fafb');
        return { ...recipe, headingId, headingDark, headingLight };
    });

const recipeBlocks = recipes.map((recipe) => {
    const base = recipe.base || 'main';
    const branch = recipe.branch || `recipe/${recipe.file.replace(/\.md$/, '')}`;
    const description = recipe.description ? `\n${recipe.description}\n` : '';
    const prompt = recipe.prompt
        ? `\n<details>\n<summary>Agent prompt</summary>\n\n\`\`\`text\n${recipe.prompt}\n\`\`\`\n\n</details>\n`
        : '';

    return `<h3 id="${recipe.headingId}"><picture><source media="(prefers-color-scheme: dark)" srcset="${headingUrl}/${recipe.headingLight}"><source media="(prefers-color-scheme: light)" srcset="${headingUrl}/${recipe.headingDark}"><img src="${headingUrl}/${recipe.headingDark}" alt="${escapeXml(recipe.title)}" height="56"></picture></h3>${description}${prompt}\nNot using an agent?\n\n- [Applied branch](../../tree/${branch})\n- [Diff](../../compare/${base}...${branch})`;
});

const strip = (text) =>
    text
        .replace(/<img[^>]*alt="([^"]+)"[^>]*>/g, '$1')
        .replace(/<[^>]+>/g, '')
        .replace(/\[([^\]]+)\]\([^)]+\)/g, '$1')
        .replace(/[`*_~]/g, '')
        .trim();

const tocFor = (markdown) => {
    const slugger = new Slugger();
    let inFence = false;
    const groups = [];

    for (const line of markdown.split('\n')) {
        if (/^(```|~~~)/.test(line)) inFence = !inFence;
        if (inFence) continue;

        const markdownHeading = /^(#{1,4})\s+(.+?)\s*#*$/.exec(line);
        const htmlHeading = /^<h([2-4])\s+id="([^"]+)"[^>]*>(.+)<\/h\1>/.exec(line);
        const level = markdownHeading ? markdownHeading[1].length : htmlHeading ? Number(htmlHeading[1]) : 0;
        if (level < 2) continue;

        const title = strip(markdownHeading ? markdownHeading[2] : htmlHeading[3]);
        const slug = markdownHeading ? slugger.slug(title) : htmlHeading[2];
        const item = level === 2 ? `[${title}](#${slug})` : `${'  '.repeat(level - 3)}- [${title}](#${slug})`;
        if (level === 2 || !groups.length) groups.push([item]);
        else groups.at(-1).push(item);
    }

    return groups.map((group) => group.join('\n')).join('\n\n');
};

const updateToc = (markdown) => {
    const toc = `<!-- toc:start -->\n${tocFor(markdown)}\n<!-- toc:end -->`;
    const marker = /<!-- toc:start -->[\s\S]*?<!-- toc:end -->/;
    if (marker.test(markdown)) return markdown.replace(marker, toc);
    return markdown.replace('[TOC]', toc);
};

const section = `<!-- recipes:start -->\n\n## Recipes\n\nThese recipes can be applied with an agent, or copied from the applied branches.\n\n${recipeBlocks.length ? recipeBlocks.join('\n\n') : '_No recipes yet._'}\n\n<!-- recipes:end -->`;
const readme = readFileSync(readmePath, 'utf8');
const marker = /<!-- recipes:start -->[\s\S]*?<!-- recipes:end -->/;
const withRecipes = marker.test(readme) ? readme.replace(marker, section) : `${readme.trimEnd()}\n\n${section}\n`;
const next = updateToc(withRecipes);
writeFileSync(readmePath, next);
console.log(`Updated ${readmePath}`);
